#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { performance } from 'node:perf_hooks';

const root = process.cwd();
const ignored = new Set(['.git', '.obsidian', '.claude', 'node_modules']);
function walk(dir) {
  const files = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (ignored.has(entry.name)) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) files.push(...walk(full));
    else if (entry.isFile() && entry.name.endsWith('.md')) files.push(full);
  }
  return files;
}

const allFiles = walk(root);
const knowledgeFiles = allFiles.filter((file) => {
  const rel = path.relative(root, file).split(path.sep).join('/');
  return !['06-Source-Library/', '00-Templates/', '_meta/', '_Inbox/'].some((prefix) => rel.startsWith(prefix));
});
const load = (files) => files.map((file) => fs.readFileSync(file, 'utf8').toLowerCase());
const allTexts = load(allFiles);
const knowledgeTexts = load(knowledgeFiles);
const queries = ['strategy', 'framework', 'growth', 'risk', 'content', 'trust', 'system'];

function benchmark(texts, rounds = 40) {
  const samples = [];
  for (let round = 0; round < rounds; round += 1) {
    const start = performance.now();
    let matches = 0;
    for (const query of queries) for (const text of texts) if (text.includes(query)) matches += 1;
    samples.push({ elapsed: performance.now() - start, matches });
  }
  samples.sort((a, b) => a.elapsed - b.elapsed);
  return { median: samples[Math.floor(samples.length / 2)].elapsed, matches: samples[0].matches };
}

const allResult = benchmark(allTexts);
const knowledgeResult = benchmark(knowledgeTexts);
const bytes = (files) => files.reduce((sum, file) => sum + fs.statSync(file).size, 0);
const fullBytes = bytes(allFiles);
const knowledgeBytes = bytes(knowledgeFiles);
const generatedOn = new Intl.DateTimeFormat('en-CA', { timeZone: 'Asia/Taipei', year: 'numeric', month: '2-digit', day: '2-digit' }).format(new Date());
const formatMb = (number) => (number / 1_000_000).toFixed(2);
const reduction = fullBytes ? ((1 - knowledgeBytes / fullBytes) * 100).toFixed(1) : '0.0';

const output = `---
title: "Search Performance"
type: "governance"
domain: "meta"
lang: "en"
generated_on: "${generatedOn}"
status: "generated"
---

# Search Performance

Repeatable structural benchmark generated by \`node tools/benchmark-vault-search.mjs\`. It compares full-vault scanning (“before” raw-source exclusion) with the documented knowledge-only scope (“after” exclusion). It is a filesystem proxy, not Obsidian's internal index timer; use it to detect material regressions on the same machine.

## Current benchmark

| Scope | Markdown files | Text size | Median seven-query scan |
|---|---:|---:|---:|
| Full vault | ${allFiles.length} | ${formatMb(fullBytes)} MB | ${allResult.median.toFixed(3)} ms |
| Knowledge-only | ${knowledgeFiles.length} | ${formatMb(knowledgeBytes)} MB | ${knowledgeResult.median.toFixed(3)} ms |

The knowledge-only scope removes **${reduction}%** of scanned text at the current archive size. Absolute timing varies with hardware and filesystem cache; file and byte counts are the durable capacity indicators.

## Reassessment triggers

- Run after adding or splitting any source larger than 1 MB.
- Run during the quarterly source-archive review.
- Investigate if source bytes or median full-vault scan time doubles from the previous recorded change.
- Consider the separate archive-vault threshold in [[Raw Source Policy]] before raw text becomes the dominant daily browsing surface.
`;

fs.writeFileSync(path.join(root, '_meta', 'Search Performance.md'), output);
console.log(`Generated _meta/Search Performance.md: ${formatMb(fullBytes)} MB full, ${formatMb(knowledgeBytes)} MB knowledge-only.`);
