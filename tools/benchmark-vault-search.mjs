#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { performance } from 'node:perf_hooks';

import { generatedOn, loadVault, recordsInScope } from './lib/vault.mjs';

const root = process.cwd();
const vault = loadVault(root);
const allRecords = vault.records;
const readerRecords = recordsInScope(vault, 'knowledge_search');
const queries = ['strategy', 'framework', 'growth', 'risk', 'content', 'trust', 'system'];

function load(records) {
  return records.map((record) => record.text.toLocaleLowerCase('en-US'));
}

function benchmark(texts, rounds = 40) {
  const samples = [];
  for (let round = 0; round < rounds; round += 1) {
    const start = performance.now();
    let matches = 0;
    for (const query of queries) for (const text of texts) if (text.includes(query)) matches += 1;
    samples.push({ elapsed: performance.now() - start, matches });
  }
  samples.sort((a, b) => a.elapsed - b.elapsed);
  return { median: samples[Math.floor(samples.length / 2)].elapsed, matches: samples[0].matches };
}

const all = benchmark(load(allRecords));
const reader = benchmark(load(readerRecords));
const date = generatedOn();
const output = `---
title: "Search Performance"
type: "governance"
domain: "meta"
lang: "en"
generated_on: "${date}"
status: "generated"
---

# Search Performance

Filesystem proxy benchmark generated by \`node tools/benchmark-vault-search.mjs\`. The exact reader corpus is selected through \`scopes.knowledge_search\` in \`_meta/vault-schema.yaml\`; it is the same positive scope as the default reader graph and includes governed reports.

| Scope | Markdown files | Median scan | Query matches |
|---|---:|---:|---:|
| Full governed vault | ${allRecords.length} | ${all.median.toFixed(3)} ms | ${all.matches} |
| Exact reader scope | ${readerRecords.length} | ${reader.median.toFixed(3)} ms | ${reader.matches} |

The result is a local proxy, not an Obsidian startup benchmark. Timing is intentionally nondeterministic; corpus membership is schema-driven and validated by \`npm run check\`.
`;

fs.writeFileSync(path.join(root, '_meta/Search Performance.md'), output);
console.log(`Generated _meta/Search Performance.md: ${readerRecords.length}/${allRecords.length} reader/full Markdown files.`);
